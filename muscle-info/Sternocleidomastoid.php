        <h2>Sternocleidomastoid</h2>
        <p>The sternocleidomastoid muscle is one of the largest and most superficial cervical muscles. The primary
            actions of the muscle are rotation of the head to the opposite side and flexion of the neck. The
            sternocleidomastoid is innervated by the accessory nerve.</p>
        <p>It is given the name sternocleidomastoid because it originates at the manubrium of the sternum (sterno-) and
            the clavicle (cleido-), and has an insertion at the mastoid process of the temporal bone of the skull.</p>

        <h3>Structure</h3>
        <p>The sternocleidomastoid muscle originates from two locations: the manubrium of the sternum and the clavicle.
            It travels obliquely across the side of the neck and inserts at the mastoid process of the temporal bone of
            the skull. The sternocleidomastoid is thick and narrow at its centre, and broader and thinner at either end.
        </p>
        <p>The sternal head is a round fasciculus, tendinous in front, fleshy behind, arising from the upper part of the
            front of the manubrium sterni. It travels superiorly, laterally, and posteriorly.</p>
        <p>The clavicular head is composed of fleshy and aponeurotic fibers, arises from the upper, frontal surface of
            the medial third of the clavicle; it is directed almost vertically upward.</p>
        <p>The two heads are separated from one another at their origins by a triangular interval (supraclavicular
            fossa) but gradually blend, below the middle of the neck, into a thick, rounded muscle which is inserted, by
            a strong tendon, into the lateral surface of the mastoid process, from its apex to its superior border, and
            by a thin aponeurosis into the lateral half of the superior nuchal line of the occipital bone.</p>

        <h3>Nerve Supply</h3>
        <p>The sternocleidomastoid is innervated by the accessory nerve of the same side. It supplies only motor fibres.
            The cervical plexus supplies sensation, including proprioception, from the ventral primary rami of C2 and
            C3.</p>

        <h3>Function</h3>
        <p>The function of this muscle is to rotate the head to the opposite side or obliquely rotate the head. It also
            flexes the neck. When both sides of the muscle act together, it flexes the neck and extends the head. When
            one side acts alone, it causes the head to rotate to the opposite side and flexes laterally to the same side
            (ipsilaterally).</p>
        <p>It also acts as an accessory muscle of respiration, along with the scalene muscles of the neck.</p>
        <p><span>Contraction</span></p>
        <p>The signaling process to contract or relax the sternocleidomastoid begins in Cranial Nerve XI, the accessory
            nerve. The accessory nerve nucleus is in the anterior horn of the spinal cord around C1-C3, where lower
            motor neuron fibers mark its origin. The fibers from the accessory nerve nucleus travel upward to enter the
            cranium via the foramen magnum. The internal carotid artery to reach both the sternocleidomastoid muscles
            and the trapezius. After a signal reaches the accessory nerve nucleus in the anterior horn of the spinal
            cord, the signal is conveyed to motor endplates on the muscle fibers located at the clavicle. Acetylcholine
            (ACH) is released from vesicles and is sent over the synaptic cleft to receptors on the postsynaptic bulb.
            The ACH causes the resting potential to increase above -55mV, thus initiating an action potential which
            travels along the muscle fiber. Along the muscle fibers are t-tubule openings which facilitate the spread of
            the action potential into the muscle fibers. The t-tubule meets with the sarcoplasmic reticulum at locations
            throughout the muscle fiber, at these locations the sarcoplasmic reticulum releases calcium ions that
            results in the movement of troponin and tropomyosin on thin filaments. The movement of troponin and
            tropomyosin is key in facilitating the myosin head to move along the thin filament, resulting in a
            contraction of the sternocleidomastoid muscle.</p>
        <p><span>Anatomical landmark</span></p>
        <p>The sternocleidomastoid is within the investing fascia of the neck, along with the trapezius muscle, with
            which it shares its nerve supply (the accessory nerve). It is thick and thus serves as a primary landmark of
            the neck, as it divides the neck into anterior and posterior cervical triangles (in front and behind the
            muscle, respectively) which helps define the location of structures, such as the lymph nodes for the head
            and neck.</p>
        <p>Many important structures relate to the sternocleidomastoid, including the common carotid artery, accessory
            nerve, and brachial plexus.</p>
