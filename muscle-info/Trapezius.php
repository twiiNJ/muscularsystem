        <h2>Trapezius</h2>
        <p>The trapezius is a large paired surface muscle that extends longitudinally from the occipital bone to the
            lower thoracic vertebrae of the spine and laterally to the spine of the scapula. It moves the scapula and
            supports the arm.</p>
        <p>The trapezius has three functional parts: an upper (descending) part which supports the weight of the arm; a
            middle region (transverse), which retracts the scapula; and a lower (ascending) part which medially rotates
            and depresses the scapula.</p>

        <h3>Structure</h3>
        <p>The trapezius muscle resembles a trapezium (trapezoid in American English), or diamond-shaped quadrilateral.
            The word "spinotrapezius" refers to the human trapezius, although it is not commonly used in modern texts.
            In other mammals, it refers to a portion of the analogous muscle. Similarly, the term "tri-axle back plate"
            was historically used to describe the trapezius muscle.</p>
        <p>The superior or upper (or descending) fibers of the trapezius originate from the spinous process of C7, the
            external occipital protuberance, the medial third of the superior nuchal line of the occipital bone (both in
            the back of the head), and the ligamentum nuchae. From this origin they proceed downward and laterally to be
            inserted into the posterior border of the lateral third of the clavicle.</p>
        <p>The middle fibers, or transverse of the trapezius arise from the spinous process of the seventh cervical
            (both in the back of the neck), and the spinous processes of the first, second, and third thoracic
            vertebrae. They are inserted into the medial margin of the acromion, and into the superior lip of the
            posterior border of the spine of the scapula.</p>
        <p>The inferior or lower (or ascending) fibers of the trapezius arise from the spinous processes of the
            remaining thoracic vertebrae (T4–T12). From this origin they proceed upward and laterally to converge near
            the scapula and end in an aponeurosis, which glides over the smooth triangular surface on the medial end of
            the spine, to be inserted into a tubercle at the apex of this smooth triangular surface.</p>
        <p>At its occipital origin, the trapezius is connected to the bone by a thin fibrous lamina, firmly adherent to
            the skin. The superficial and deep epimysia are continuous with an investing deep fascia that encircles the
            neck and also contains both sternocleidomastoid muscles.</p>
        <p>At the middle, the muscle is connected to the spinous processes by a broad semi-elliptical aponeurosis, which
            reaches from the sixth cervical to the third thoracic vertebræ and forms, with that of the opposite muscle,
            a tendinous ellipse. The rest of the muscle arises by numerous short tendinous fibers.</p>
        <p>It is possible to feel the muscles of the superior trapezius become active by holding a weight in one hand in
            front of the body and, with the other hand, touching the area between the shoulder and the neck.</p>

        <h3>Nerve Supply</h3>
        <p>Motor function is supplied by the accessory nerve. Sensation, including pain and the sense of joint position
            (proprioception), travel via the ventral rami of the third (C3) and fourth (C4) cervical nerves. Since it is
            a muscle of the upper limb, the trapezius is not innervated by dorsal rami despite being placed
            superficially in the back.</p>

        <h3>Function</h3>
        <p>Contraction of the trapezius muscle can have two effects: movement of the scapulae when the spinal origins
            are stable, and movement of the spine when the scapulae are stable. Its main function is to stabilize and
            move the scapula.</p>
        <p><span>Scapular movements</span></p>
        <p>The upper fibers elevate the scapulae, the middle fibers retract the scapulae, and the lower fibers depress
            the scapulae.</p>
        <p>In addition to scapular translation, the trapezius induces scapular rotation. The upper and lower fibers tend
            to rotate the scapula around the sternoclavicular articulation so that the acromion and inferior angles move
            up and the medial border moves down (upward rotation). The upper and lower fibers work in tandem with
            serratus anterior to upwardly rotate the scapulae, and work in opposition to the levator scapulae and the
            rhomboids, which effect downward rotation.</p>
        <p>An example of trapezius function is an overhead press. When activating together, the upper and lower fibers
            also assist the middle fibers (along with other muscles such as the rhomboids) with scapular
            retraction/adduction.</p>
        <p>The trapezius also assists in abduction of the shoulder above 90 degrees by rotating the glenoid upward.
            Injury to cranial nerve XI will cause weakness in abducting the shoulder above 90 degrees.</p>

        <p><span>Spinal movements</span></p>
        <p>When the scapulae are stable a co-contraction of both sides can extend the neck.</p>

