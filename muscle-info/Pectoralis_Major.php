        <h2>Pectoralis Major</h2>
        <p>The pectoralis major (from Latin pectus, meaning 'breast') is a thick, fan-shaped muscle, situated at the
            chest of the human body. It makes up the bulk of the chest muscles and lies under the breast. Beneath the
            pectoralis major is the pectoralis minor, a thin, triangular muscle. The pectoralis major's primary
            functions are flexion, adduction, and internal rotation of the humerus. The pectoral major may colloquially
            be referred to as "pecs", "pectoral muscle" or "chest muscle" due to it being the largest and most
            superficial muscle in the chest area.</p>

        <h3>Structure</h3>
        <p>It arises from the anterior surface of the sternal half of the clavicle from breadth of the half of the
            anterior surface of the sternum, as low down as the attachment of the cartilage of the sixth or seventh rib;
            from the cartilages of all the true ribs, with the exception, frequently, of the first or seventh, and from
            the aponeurosis of the abdominal external oblique muscle.</p>
        <p>From this extensive origin the fibers converge toward their insertion; those arising from the clavicle pass
            obliquely downward and outwards (laterally), and are usually separated from the rest by a slight interval;
            those from the lower part of the sternum, and the cartilages of the lower true ribs, run upward and
            laterally, while the middle fibers pass horizontally.</p>
        <p>They all end in a flat tendon, about 5 cm in breadth, which is inserted into the lateral lip of the bicipital
            groove (intertubercular sulcus) of the humerus.</p>
        <p>This tendon consists of two laminae, placed one in front of the other, and usually blended together below:
        </p>
        <ul>
            <li>The anterior lamina, which is thicker, receives the clavicular and the uppermost sternal fibers. They
                are inserted in the same order as that in which they arise: the most lateral of the clavicular fibers
                are inserted at the upper part of the anterior lamina; the uppermost sternal fibers pass down to the
                lower part of the lamina which extends as low as the tendon of the Deltoid and joins with it.</li>
            <li>The posterior lamina of the tendon receives the attachment of the greater part of the sternal portion
                and the deep fibers, i. e., those from the costal cartilages.</li>
        </ul>
        <p>These deep fibers, and particularly those from the lower costal cartilages, ascend the humerus insertion
            higher, turning backward successively behind the superficial and upper ones, so that the tendon appears to
            be twisted. The posterior lamina reaches higher on the humerus than the anterior one, and from it an
            expansion is given off which covers the intertubercular groove of the humerus and blends with the capsule of
            the shoulder-joint.</p>
        <p>From the deepest fibers of this lamina at its insertion an expansion is given off which lines the
            intertubercular groove, while from the lower border of the tendon a third expansion passes downward to the
            fascia of the arm.</p>

        <h3>Nerve Supply</h3>
        <p>The pectoralis major receives dual motor innervation by the medial pectoral nerve and the lateral pectoral
            nerve, also known as the lateral anterior thoracic nerve. The sternal head receives innervation from the C7,
            C8 and T1 nerve roots, via the lower trunk of the brachial plexus and the medial pectoral nerve. The
            clavicular head receives innervation from the C5 and C6 nerve roots via the upper trunk and lateral cord of
            the brachial plexus, which gives off the lateral pectoral nerve. The lateral pectoral nerve is distributed
            over the deep surface of the pectoralis major.</p>
        <p>The sensory feedback from the pectoralis major follows the reverse path, returning via first-order neurons to
            the spinal nerves at C5, C6, C8, and T1 through the posterior rami. After the synapse in the posterior horn
            of the spinal cord, sensory information concerning movement of the muscle, proprioception, and pressure then
            travels through a second-order neuron in the dorsal column medial lemniscus tract to the medulla. There, the
            fibers decussate to form the medial lemniscus which carries the sensory information the rest of the way to
            the thalamus, the "gateway to the cortex". The thalamus diverts some sensory information to the cerebellum
            and the basal nuclei to complete the motor feedback loop while some sensory information ascends directly to
            the postcentral gyrus of the parietal lobe of the brain via third-order neurons. Sensory information for the
            pectoralis major is processed in the superior portion of the sensory homunculus, adjacent to the
            longitudinal fissure which divides the two hemispheres of the brain.</p>
        <p>Electromyography suggests that it consists of at least six groups of muscle fibres that can be independently
            coordinated by the central nervous system.</p>

        <h3>Function</h3>
        <p>The pectoralis major has four actions which are primarily responsible for movement of the shoulder joint. The
            first action is flexion of the humerus, as in throwing a ball underhand, and in lifting a child. Secondly,
            it adducts the humerus, as when flapping the arms. Thirdly, it rotates the humerus medially, as occurs when
            arm-wrestling. Fourthly the pectoralis major is also responsible for keeping the arm attached to the trunk
            of the body. It has two different parts which are responsible for different actions. The clavicular part is
            close to the deltoid muscle and contributes to flexion, horizontal adduction, and inward rotation of the
            humerus. When at an approximately 110 degree angle, it contributes to adduction of the humerus. The
            sternocostal part is antagonistic to the clavicular part contributing to downward and forward movement of
            the arm and inward rotation when accompanied by adduction. The sternal fibers can also contribute to
            extension, but not beyond anatomical position.</p>
        <p>Hypertrophy of the pectoralis major increases functionality. Maximal activation of the pectoralis major
            occurs in the transverse plane through pressing motions. Both multi-joint and single-joint exercises induce
            pectoralis major hypertrophy. A combination of both single-joint and multi-joint exercises will result in a
            maximum hypertrophic response. [Aesthetic contours of regions in the muscle may be specifically-addressed by
            specific exercises; for instance, “plating” or “stitching” of the pectoralis major —towards the center of
            the sternum —-may be targeted by a wider hand position.] The pectoralis major can be targeted from numerous
            training angles along the sternum and clavicle. Exercises that include horizontal adduction and elbow
            extensions such as the barbell bench press, dumbbell bench press, and machine bench press induce high
            activation of the pectoralis major in the sternocostal region. Heavy loads are strongly correlated with
            pectoralis major activation.</p>

