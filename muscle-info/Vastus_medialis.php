        <div class="exercise">
            <h3>Zottman Curl</h3>
            <p>This multi-movement piece is one of the best bicep exercises you can add to your arm arsenal. The
                Zottman Curl focuses on all three major muscles that make up the biceps – the biceps brachii,
                brachialis and the brachioradialis. To perform the Zottman Curl;</p>
            <ol>
                <li>Hold arms at your side with a dumbbell in each hand.</li>
                <li>Turn your arms so your palms face forward.</li>
                <li>Bend your elbows and curl the dumbbells close to your shoulders without moving your upper arms.
                </li>
                <li>Pause, then rotate the dumbbells so your palms face forward again.</li>
                <li>Slowly lower the weights down in that position.</li>
                <li>Rotate the dumbbells back to the starting position.</li>
                <li>Repeat.</li>
            </ol>
