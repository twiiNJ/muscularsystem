        <h2>Vastus Medialis</h2>
        <p>The vastus medialis (vastus internus or teardrop muscle) is an extensor muscle located medially in the thigh
            that extends the knee. The vastus medialis is part of the quadriceps muscle group.</p>

        <h3>Structure</h3>
        <ul>
            <li>The vastus medialis is a muscle present in the anterior compartment of thigh, and is one of the four
                muscles that make up the quadriceps muscle. The others are the vastus lateralis, vastus intermedius and
                rectus femoris. It is the most medial of the "vastus" group of muscles. The vastus medialis arises
                medially along the entire length of the femur, and attaches with the other muscles of the quadriceps in
                the quadriceps tendon.</li>
            <li>The vastus medialis muscle originates from a continuous line of attachment on the femur, which begins on
                the front and middle side (anteromedially) on the intertrochanteric line of the femur. It continues down
                and back (posteroinferiorly) along the pectineal line and then descends along the inner (medial) lip of
                the linea aspera and onto the medial supracondylar line of the femur. The fibers converge onto the inner
                (medial) part of the quadriceps tendon and the inner (medial) border of the patella.</li>
            <li>The obliquus genus muscle is the most distal segment of the vastus medialis muscle. Its specific
                training plays an important role in maintaining patella position and limiting injuries to the knee. With
                no clear delineation, it is simply the most distal group of fibers of the vastus medialis.</li>
        </ul>

        <h3>Nerve Supply</h3>
        <p>Femoral nerve</p>

        <h3>Function</h3>
        <p>The vastus medialis is one of four muscles in the anterior compartment of the thigh. It is involved in knee
            extension, along with the other muscles which make up the quadriceps muscle. The vastus medialis also
            contributes to correct tracking of the patella.</p>
        <p>A division of the vastus medialis muscle into two groups of fibers has been hypothesized, a long and
            relatively inline group of fibres with the quadriceps ligament, the vastus medialis longus; and a shorter
            and more obliquely oriented with group of fibres, the vastus medialis obliquus. There is as yet insufficient
            evidence to conclusively confirm or deny this hypothesis.</p>

