        <h2>Tabialis Anterior</h2>
        <p>The tibialis anterior is a muscle in humans that originates along the upper two-thirds of the lateral
            (outside) surface of the tibia and inserts into the medial cuneiform and first metatarsal bones of the foot.
            It acts to dorsiflex and invert the foot. This muscle is mostly located near the shin.</p>
        <p>It is situated on the lateral side of the tibia; it is thick and fleshy above, tendinous below. The tibialis
            anterior overlaps the anterior tibial vessels and deep peroneal nerve in the upper part of the leg.</p>

        <h3>Structure</h3>
        <p>It arises from the lateral condyle and upper half or two-thirds of the lateral surface of the body of the
            tibia; from the adjoining part of the interosseous membrane; from the deep surface of the fascia; and from
            the intermuscular septum between it and the extensor digitorum longus.</p>
        <p>The fibers of this circumpennate muscle are relatively parallel to the plane of insertion, ending in a
            tendon, apparent on the anteriomedial dorsal aspect of the foot close to the ankle.</p>
        <p>After passing through the most medial compartments of the transverse and cruciate crural ligaments, it is
            inserted into the medial and under surface of the medial cuneiform bone and the base of the first metatarsal
            bone.</p>

        <h3>Nerve Supply</h3>
        <p>Deep peroneal (fibular) nerve, branch of common peroneal (fibular) nerve (L4, L5, S1).</p>

        <h3>Function</h3>
        <p>The tibialis anterior muscle is the most medial muscle of the anterior compartment of the leg. It is
            responsible for dorsiflexing and inverting the foot. The muscle has two origins, one being the lateral
            tibial condyle and the other being the upper lateral surface of the tibia, and inserts on the medial surface
            of the medial cuneiform and adjoining part of base of the first metatarsal of the foot allowing the toe to
            be pulled up and held in a locked position. It also allows for the ankle to be inverted giving the ankle
            horizontal movement allowing for some cushion if the ankle were to be rolled. It is innervated by the deep
            peroneal nerve and acts as both an antagonist and a synergist of the tibialis posterior. However, the most
            accurate antagonist of the tibialis anterior is the peroneus longus. The tibialis anterior aides in the
            activities of walking, running, hiking, kicking a ball, or any activity that requires moving the leg or
            keeping the leg vertical. It functions to stabilize the ankle as the foot hits the ground during the contact
            phase of walking (eccentric contraction) and acts later to pull the foot clear of the ground during the
            swing phase (concentric contraction). It also functions to 'lock' the ankle, as in toe-kicking a ball, when
            held in an isometric contraction.</p>
        <p>Antagonists are plantar-flexors of the posterior compartment such as soleus and gastrocnemius.</p>
        <p>The movements of tibialis anterior are dorsiflexion and inversion of the ankle. However, actions of tibialis
            anterior are dependent on whether the foot is weight bearing or not (closed or open kinetic chain). When the
            foot is on the ground, the muscle helps to balance the leg and talus on the other tarsal bones so that the
            leg is kept vertical even when walking on uneven ground.</p>

