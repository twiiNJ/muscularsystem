        <div class="exercise">
            <h3>Alternating Incline Dumbbell Curl</h3>
            <p>If you really want to add some size to that bicep bulge, one of the best bicep exercises to test out
                is the Alternating Incline Dumbbell Curl. This variation helps to strengthen the upper section of
                your biceps brachii, helping you to achieve that large muscle growth you’ve been chasing. To perform
                the Alternating Incline Dumbbell Curl properly;</p>
            <ol>
                <li>On an incline bench, lie back with a dumbbell in each hand and palms facing forward.</li>
                <li>Keeping your elbow and shoulder stationary, slowly lift one weight to the front of your
                    shoulder.</li>
                <li>Pause, squeezing your bicep at the top.</li>
                <li>Slowly reverse the movement to return to start.</li>
                <li>Repeat on the opposite side.</li>
            </ol>

