        <h2>Brachioradialis</h2>
        <p>The brachioradialis is a muscle of the forearm that flexes the forearm at the elbow. It is also capable of
            both pronation and supination, depending on the position of the forearm. It is attached to the distal
            styloid process of the radius by way of the brachioradialis tendon, and to the lateral supracondylar ridge
            of the humerus.
        </p>

        <h3>Structure</h3>
        <p>The brachioradialis is a superficial, fusiform muscle on the lateral side of the forearm that attaches
            proximally on the lateral supracondylar ridge of the humerus and distally on the radius, close to its
            styloid process. Near the elbow, it forms the lateral limit of the cubital fossa, or elbow pit.</p>

        <h3>Nerve Supply</h3>
        <p>Despite the bulk of the muscle body being visible from the anterior aspect of the forearm, the
            brachioradialis is a posterior compartment muscle and consequently is innervated by the radial nerve. Of the
            muscles that receive innervation from the radial nerve, it is one of only four that receive input directly
            from the radial nerve. The other three are the triceps, anconeus, and extensor carpi radialis longus. All
            other posterior compartment muscles that receive radial innervation are supplied by the deep branch of the
            radial nerve.</p>

        <h3>Function</h3>
        <p>The brachioradialis flexes the forearm at the elbow. When the forearm is pronated, the brachioradialis tends
            to supinate as it flexes. In a supinated position, it tends to pronate as it flexes. This also assists the
            biceps brachii.</p>
        <p>The brachioradialis is a stronger elbow flexor when the forearm is in a midposition between supination and
            pronation at the radioulnar joint. When pronated, the brachioradialis is more active during elbow flexion
            since the biceps brachii is in a mechanical disadvantage.</p>
        <p>With the insertion of the muscle so far from the fulcrum of the elbow, the brachioradialis does not generate
            as much joint torque as the brachialis or the biceps. It is effective mainly when those muscles have already
            partially flexed at the elbow. The brachioradialis flexes the forearm at the elbow, especially when quick
            movement is required and when a weight is lifted during slow flexion of the forearm.</p>
        <p>The muscle is used to stabilize the elbow during rapid flexion and extension while in a midposition, such as
            in hammering. The brachioradialis is synergistic with the brachialis and biceps brachii; the triceps brachii
            and anconeus are antagonistic.</p>

