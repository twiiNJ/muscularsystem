        <h2>Rectus Femoris</h2>
        <p>The rectus femoris muscle is one of the four quadriceps muscles of the human body. The others are the vastus
            medialis, the vastus intermedius (deep to the rectus femoris), and the vastus lateralis. All four parts of
            the quadriceps muscle attach to the patella (knee cap) by the quadriceps tendon.</p>
        <p>The rectus femoris is situated in the middle of the front of the thigh; it is fusiform in shape, and its
            superficial fibers are arranged in a bipenniform manner, the deep fibers running straight down to the deep
            aponeurosis. Its functions are to flex the thigh at the hip joint and to extend the leg at the knee joint.
        </p>

        <h3>Structure</h3>
        <p>It arises by two tendons: one, the anterior or straight, from the anterior inferior iliac spine; the other,
            the posterior or reflected, from a groove above the rim of the acetabulum.</p>
        <p>The two unite at an acute angle and spread into an aponeurosis that is prolonged downward on the anterior
            surface of the muscle, and from this the muscular fibers arise.</p>
        <p>The muscle ends in a broad and thick aponeurosis that occupies the lower two-thirds of its posterior surface,
            and, gradually becoming narrowed into a flattened tendon, is inserted into the base of the patella.</p>

        <h3>Nerve Supply</h3>
        <p>The neurons for voluntary thigh contraction originate near the summit of the medial side of the precentral
            gyrus (the primary motor area of the brain). These neurons send a nerve signal that is carried by the
            corticospinal tract down the brainstem and spinal cord. The signal starts with the upper motor neurons
            carrying the signal from the precentral gyrus down through the internal capsule, through the cerebral
            peduncle, and into the medulla. In the medullary pyramid, the corticospinal tract decussates and becomes the
            lateral corticospinal tract. The nerve signal will continue down the lateral corticospinal tract until it
            reaches spinal nerve L4. At this point, the nerve signal will synapse from the upper motor neurons to the
            lower motor neurons. The signal will travel through the anterior root of L4 and into the anterior rami of
            the L4 nerve, leaving the spinal cord through the lumbar plexus. The posterior division of the L4 root is
            the Femoral nerve. The femoral nerve innervates the quadriceps femoris, a fourth of which is the rectus
            femoris. When the rectus femoris receives the signal that has traveled all the way from the medial side of
            the precentral gyrus, it contracts, extending the knee and flexing the thigh at the hip.</p>

        <h3>Function</h3>
        <p>The rectus femoris, sartorius, and iliopsoas are the flexors of the thigh at the hip. The rectus femoris is a
            weaker hip flexor when the knee is extended because it is already shortened and thus suffers from active
            insufficiency; the action will recruit more iliacus, psoas major, tensor fasciae latae, and the remaining
            hip flexors than it will the rectus femoris.</p>
        <p>Similarly, the rectus femoris is not dominant in knee extension when the hip is flexed since it is already
            shortened and thus suffers from active insufficiency. In essence: the action of extending the knee from a
            seated position is primarily driven by the vastus lateralis, vastus medialis, and vastus intermedius, and
            less by the rectus femoris.</p>
        <p>In the other extreme, the muscle's ability to flex the hip and extend the knee can be compromised in a
            position of full hip extension and knee flexion, due to passive insufficiency.</p>
        <p>The rectus femoris is a direct antagonist to the hamstrings, at the hip and at the knee.</p>

