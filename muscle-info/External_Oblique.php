        <h2>External Oblique</h2>
        <p>The abdominal external oblique muscle (also external oblique muscle, or exterior oblique) is the largest and
            outermost of the three flat abdominal muscles of the lateral anterior abdomen.</p>

        <h3>Structure</h3>
        <p>The external oblique is situated on the lateral and anterior parts of the abdomen. It is broad, thin, and
            irregularly quadrilateral, its muscular portion occupying the side, its aponeurosis the anterior wall of the
            abdomen. In most humans (especially females), the oblique is not visible, due to subcutaneous fat deposits
            and the small size of the muscle.</p>
        <p>It arises from eight fleshy digitations, each from the external surfaces and inferior borders of the fifth to
            twelfth ribs (lower eight ribs). These digitations are arranged in an oblique line which runs inferiorly and
            anteriorly, with the upper digitations being attached close to the cartilages of the corresponding ribs, the
            lowest to the apex of the cartilage of the last rib, the intermediate ones to the ribs at some distance from
            their cartilages.</p>
        <p>The five superior serrations increase in size from above downward, and are received between corresponding
            processes of the serratus anterior muscle; the three lower ones diminish in size from above downward and
            receive between them corresponding processes from the latissimus dorsi. From these attachments the fleshy
            fibers proceed in various directions. Its posterior fibers from the ribs to the iliac crest form a free
            posterior border.</p>
        <p>Those from the lowest ribs pass nearly vertically downward, and are inserted into the anterior half of the
            outer lip of the iliac crest; the middle and upper fibers, directed downward (inferiorly) and forward
            (anteriorly), become aponeurotic at approximately the midclavicular line and forms the anterior layer of the
            rectus sheath. This aponeurosis formed from fibres from either side of the external oblique decussates at
            the linea alba.</p>
        <p>The aponeurosis of the external oblique muscle forms the inguinal ligament. The muscle also contributes to
            the inguinal canal.</p>
        <p>Just deep to the external oblique is the internal oblique muscle. These muscles are in the deepest layer of
            the abdominal wall.</p>

        <h3>Nerve Supply</h3>
        <p>The external oblique muscle is supplied by ventral branches of the lower six thoracoabdominal nerves and the
            subcostal nerve on each side.</p>

        <h3>Function</h3>
        <p>The external oblique functions to pull the chest downwards and compress the abdominal cavity, which increases
            the intra-abdominal pressure as in a valsalva maneuver. It also performs ipsilateral (same side)
            side-bending and contralateral (opposite side) rotation. So the right external oblique would side bend to
            the right and rotate to the left. The internal oblique muscle functions similarly except it rotates
            ipsilaterally.</p>

