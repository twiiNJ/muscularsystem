            <h3>Standing Cable Curl</h3>
            <p>Forget what you think you know about cable-work. Cables are actually a great way to work out
                different sections of your body, in particular, the bicep brachioradialis. The Standing Cable Curl
                allows you to keep consistent resistance, increasing the strain on your muscles and giving you a far
                more efficient bicep workout. To perform Standing Cable Curls;</p>
            <ol>
                <li>Stand with feet shoulder-width apart facing a cable machine with the machine’s handle on the
                    lowest setting.</li>
                <li>Hold the handle with both hands palm facing forward and arms extended straight to the floor.
                </li>
                <li>Keeping your elbows and shoulders stationary, slowly lift the weight to the front of your
                    shoulders.</li>
                <li>Pause, squeezing your biceps at the top</li>
                <li>Slowly reverse the movement to return to start.</li>
                <li>Repeat on the opposite side.</li>
