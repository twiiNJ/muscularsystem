        <h2>Deltoid</h2>
        <p>The deltoid muscle is the muscle forming the rounded contour of the human shoulder. It is also known as the
            'common shoulder muscle', particularly in other animals such as the domestic cat. Anatomically, it appears
            to be made up of three distinct sets of fibers, namely 1. anterior or clavicular part (pars clavicularis) 2.
            posterior or scapular part (pars scapularis) 3. intermediate or acromial part (pars acromialis). However,
            electromyography suggests that it consists of at least seven groups that can be independently coordinated by
            the nervous system.</p>
        <p>It was previously called the deltoideus (plural deltoidei) and the name is still used by some anatomists. It
            is called so because it is in the shape of the Greek capital letter delta (Δ). Deltoid is also further
            shortened in slang as "delt".</p>
        <p>A study of 30 shoulders revealed an average mass of 191.9 grams (6.77 oz) in humans, ranging from 84 grams
            (3.0 oz) to 366 grams (12.9 oz).</p>

        <h3>Structure</h3>
        <p>Previous studies showed that the insertions of the tendons of the deltoid muscle parts formed three discrete
            sets of muscle fibers, often referred to as "heads":</p>
        <ol>
            <li>The anterior or clavicular fibers arise from most of the anterior border and upper surface of the
                lateral third of the clavicle. The anterior origin lies adjacent to the lateral fibers of the
                pectoralis major muscle as do the end tendons of both muscles. These muscle fibers are closely related
                and only a small chiasmatic space, through which the cephalic vein passes, prevents the two muscles from
                forming a continuous muscle mass.
                <ul>
                    <li>The anterior deltoids are commonly called front delts for short.</li>
                </ul>
            </li>
            <li>
                Intermediate or acromial fibers arise from the superior surface of the acromion process of the scapula.
                <ul>
                    <li>They are mistakenly commonly called lateral deltoid. This muscle is also called middle delts,
                        outer delts, or side delts for short.</li>
                    <li>They are also mistakenly called medial deltoid, which is wrong, as their origin is the least
                        medial portion of the deltoid.</li>
                </ul>
            </li>
            <li>
                Posterior or spinal fibers arise from the lower lip of the posterior border of the spine of the scapula.
                <ul>
                    <li>They are commonly called posterior deltoid or rear deltoid (rear delts for short).</li>
                </ul>
            </li>
        </ol>
        <p>Fick divided these three groups of fibers, often referred to as parts (Latin: pars) or bands, into seven
            functional components as did Kapandji and Sakoma Y et al. : the anterior part has two components (I
            and II); the lateral one (III); and the posterior four (IV, V, VI, and VII) components. In standard
            anatomical position (with the upper limb hanging alongside the body), the central components (II, III, and
            IV) lie lateral to the axis of abduction and therefore contribute to abduction from the start of the
            movement while the other components (I, V, VI, and VII) then act as adductors. During abduction most of
            these latter components (except VI and VII which always act as adductors) are displaced laterally and
            progressively start to abduct.</p>

        <h3>Nerve Supply</h3>
        <p>The deltoid is innervated by the axillary nerve. The axillary nerve originates from the anterior rami of the
            cervical nerves C5 and C6, via the superior trunk, posterior division of the superior trunk, and the
            posterior cord of the brachial plexus.</p>
        <p>Studies have shown that there are seven neuromuscular segments to the deltoid muscle. Three of these lie in
            the anatomical anterior head of the deltoid, one in the anatomical middle head, and three in the anatomical
            posterior head of the deltoid. These neuromuscular segments are supplied by smaller branches of the axillary
            nerve, and work in coordination with other muscles of the shoulder girdle include pectoralis major and
            supraspinatus.</p>
        <p>The axillary nerve is sometimes damaged during surgical procedures of the axilla, such as for breast cancer.
            It may also be injured by anterior dislocation of the head of the humerus.</p>

        <h3>Function</h3>
        <p>When all its fibers contract simultaneously, the deltoid is the prime mover of arm abduction along the
            frontal plane. The arm must be medially rotated for the deltoid to have maximum effect. This makes the
            deltoid an antagonist muscle of the pectoralis major and latissimus dorsi during arm adduction.</p>
        <p>The anterior fibers assist the pectoralis major to flex the shoulder. The anterior deltoid also works in
            tandem with the subscapularis, pecs and lats to internally (medially) rotate the humerus.</p>
        <p>The intermediate fibers perform basic shoulder abduction when the shoulder is internally rotated, and perform
            shoulder transverse abduction when the shoulder is externally rotated. They are not utilized significantly
            during strict transverse extension (shoulder internally rotated) such as in rowing movements, which use the
            posterior fibers.</p>
        <p>The posterior fibers assist the latissimus dorsi to extend the shoulder. Other transverse extensors, the
            infraspinatus and teres minor, also work in tandem with the posterior deltoid as external (lateral)
            rotators, antagonists to strong internal rotators like the pecs and lats.</p>
        <p>An important function of the deltoid in humans is preventing the dislocation of the humeral head when a
            person carries heavy loads. The function of abduction also means that it would help keep carried objects a
            safer distance away from the thighs to avoid hitting them, as during a farmer's walk. It also ensures a
            precise and rapid movement of the glenohumeral joint needed for hand and arm manipulation. The intermediate
            fibers are in the most efficient position to perform this role, though like basic abduction movements (such
            as lateral raise) it is assisted by simultaneous co-contraction of anterior/posterior fibers.</p>
        <p>The deltoid is responsible for elevating the arm in the scapular plane and its contraction in doing this also
            elevates the humeral head. To stop this compressing against the undersurface of the acromion the humeral
            head and injuring the supraspinatus tendon, there is a simultaneous contraction of some of the muscles of
            the rotator cuff: the infraspinatus and subscapularis primarily perform this role. In spite of this there
            may be still a 1–3 mm upward movement of the head of the humerus during the first 30° to 60° of arm
            elevation.</p>

