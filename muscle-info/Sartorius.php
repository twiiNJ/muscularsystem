        <h2>Sartorius</h2>
        <p>The sartorius muscle is the longest muscle in the human body. It is a long, thin, superficial muscle that
            runs down the length of the thigh in the anterior compartment.</p>
        <p>The name Sartorius comes from the Latin word sartor, meaning tailor, and it is sometimes called the tailor's
            muscle. This name was chosen in reference to the cross-legged position in which tailors once sat. In
            French, a muscle name itself "couturier" comes from this specific position which is referred to as "sitting
            as a tailor". There are other hypotheses as to the genesis of the
            name. One is that it refers to the location of the inferior portion of the muscle being the "inseam" or area
            of the inner thigh that tailors commonly measure when fitting trousers. Another is that the muscle closely
            resembles a tailor's ribbon. Additionally, antique sewing machines required continuous crossbody pedaling.
            This combination of lateral rotation and flexion of the hip and flexion of the knee gave tailors
            particularly developed sartorius muscles.</p>

        <h3>Structure</h3>
        <p>The sartorius muscle originates from the anterior superior iliac spine and part of the notch between the
            anterior superior iliac spine and anterior inferior iliac spine. It runs obliquely across the upper and
            anterior part of the thigh in an inferomedial direction. It passes behind the medial condyle of the femur
            to end in a tendon. This tendon curves anteriorly to join the tendons of the gracilis and semitendinosus
            muscles in the pes anserinus, where it inserts into the superomedial surface of the tibia.</p>
        <p>Its upper portion forms the lateral border of the femoral triangle, and the point where it crosses adductor
            longus marks the apex of the triangle. Deep to sartorius and its fascia is the adductor canal, through which
            the saphenous nerve, femoral artery and vein, and nerve to vastus medialis pass.</p>

        <h3>Nerve Supply</h3>
        <p>Like the other muscles in the anterior compartment of the thigh, sartorius is innervated by the femoral
            nerve.</p>

        <h3>Function</h3>
        <p>The sartorius muscle can move the hip joint and the knee joint, but all of its actions are weak, making it a
            synergist muscle. At the hip, it can flex, weakly abduct, and laterally rotate the thigh. At the knee, it
            can flex the leg; when the knee is flexed, sartorius medially rotates the leg. Turning the foot to look at
            the sole or sitting cross-legged demonstrates all four actions of the sartorius.</p>

