        <h2>Rectus Abdominus</h2>
        <p>The rectus abdominis muscle, also known as the "abdominal muscle", is a paired muscle running vertically on
            each side of the anterior wall of the human abdomen, as well as that of some other mammals. There are two
            parallel muscles, separated by a midline band of connective tissue called the linea alba. It extends from
            the pubic symphysis, pubic crest and pubic tubercle inferiorly, to the xiphoid process and costal cartilages
            of ribs V to VII superiorly. The proximal attachments are the pubic crest and the pubic symphysis. It
            attaches distally at the costal cartilages of ribs 5-7 and the xiphoid process of the sternum.</p>
        <p>The rectus abdominis muscle is contained in the rectus sheath, which consists of the aponeuroses of the
            lateral abdominal muscles. Bands of connective tissue called the tendinous intersections traverse the rectus
            abdominis, which separates this parallel muscle into distinct muscle bellies. The outer, most lateral line,
            defining the "abs" is the linea semilunaris. In the abdomens of people with low body fat, these muscle
            bellies can be viewed externally and are commonly referred to as "four", "six", "eight", or "ten packs",
            depending on how many are visible; although, six is the most common.</p>

        <h3>Structure</h3>
        <p>The rectus abdominis is a long flat muscle, which extends along the whole length of the front of the abdomen,
            and is separated from its fellow of the opposite side by the linea alba. Tendinous intersections
            (incriptiones tendinae) further subdivide each rectus abdominis muscle into a series of smaller false muscle
            bellies. Tensing of the rectus abdominis causes the muscle to expand between each tendinous intersection,
            resulting in the characteristic six or eight pack observed in individuals with low body fat.</p>
        <p>The upper portion, attached principally to the cartilage of the fifth rib, usually has some fibers of
            insertion into the anterior extremity of the rib itself.</p>
        <p><span>Size</span></p>
        <p>It is typically around 10 mm thick (compared to 20 mm thick superficial fat), or 20 mm thick in young
            athletes such as handball players. Typical volume is around 300 cm³ in non-active individuals, or almost 500
            cm³ in athletes.</p>

        <h3>Nerve Supply</h3>
        <p>The muscles are innervated by thoraco-abdominal nerves, these are continuations of the T7-T11 intercostal
            nerves and pierce the anterior layer of the rectus sheath. Sensory supply is from the 7-12 thoracic nerves
        </p>

        <h3>Function</h3>
        <p>The rectus abdominis is an important postural muscle. It is responsible for flexing the lumbar spine, as when
            doing a so-called "crunch" sit up. The rib cage is brought up to where the pelvis is when the pelvis is
            fixed, or the pelvis can be brought towards the rib cage (posterior pelvic tilt) when the rib cage is fixed,
            such as in a leg-hip raise. The two can also be brought together simultaneously when neither is fixed in
            space.</p>
        <p>The rectus abdominis assists with breathing and plays an important role in respiration when forcefully
            exhaling, as seen after exercise as well as in conditions where exhalation is difficult such as emphysema.
            It also helps in keeping the internal organs intact and in creating intra-abdominal pressure, such as when
            exercising or lifting heavy weights, during forceful defecation or parturition (childbirth).</p>

