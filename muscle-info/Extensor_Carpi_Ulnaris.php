        <h2>Extensor Carpi Ulnaris</h2>
        <p>In human anatomy, the extensor carpi ulnaris is a skeletal muscle located on the ulnar side of the forearm.
            It acts to extend and adduct at the carpus/wrist from anatomical position.

            Being an extensor muscle, extensor carpi ulnaris is on the posterior side of the forearm.</p>

        <h3>Structure</h3>
        <p>The extensor carpi ulnaris serves to extend and adduct the hand at the wrist and also provides medial
            stability to the wrist. It is a thin muscle which has fibers originating from both the distal humerus, as a
            part of the common extensor tendon, as well as the proximal ulna.</p>

        <h3>Function</h3>
        <p>The oblique course of extensor carpi ulnaris orientates its direction of pull posterolaterally, meaning that
            its contraction results with a combined extension and adduction (ulnar deviation) of the hand.
        </p>
        <p>
            Working together with extensor carpi radialis brevis and extensor carpi radialis longus, the extensor carpi
            ulnaris muscle contributes to a balanced extension of wrist without deviating the hand in the transverse
            plane. This action is important for activities of the hand, such as strongly clenching a fist or making a
            power grip. In this process, extension of the wrist blocks the forearm flexors from on acting upon it.
            Instead, these flexors remain able to act solely on the digits, flexing them to produce an effective grip
            such as that seen in a tennis backhand.
        </p>

        <p>
            Similarly, synergistic action of flexor carpi ulnaris and extensor carpi ulnaris results in balanced
            adduction (ulnar deviation) of the hand at the wrist. That is, adduction without added flexion or extension.
            This action is necessary for processes like hammering, throwing, golfing and swinging a baseball bat.
            Extensor carpi ulnaris also contributes to the stability of the distal radioulnar joint.
        </p>
