            <h3>Standing Barbell Curl</h3>
            <p>Once you’ve got the more complex curl variations out of the way, you should return back to the old
                faithful. The Standing Barbell Curl is traditionally the most common bicep exercise, and for good
                reason. Barbells allow you to work both arms simultaneously and evenly, which is great for stability
                and control. To perform a Standing Barbell Curl;</p>
            <ol>
                <li>Standing tall, hold the barbell with a wide, underhand grip.</li>
                <li>Starting with the bar at hip height, squeeze your core and contract your biceps to curl the bar
                    up to shoulder height.</li>
                <li>Keeping your elbows and shoulders stationary, slowly lift the weight to the front of your
                    shoulders.</li>
                <li>Pause, squeezing your biceps at the top</li>
                <li>Slowly reverse the movement to return to start.</li>
                <li>Repeat on the opposite side.</li>
