        <h2>Soleus</h2>
        <p>In humans and some other mammals, the soleus is a powerful muscle in the back part of the lower leg (the
            calf). It runs from just below the knee to the heel, and is involved in standing and walking. It is closely
            connected to the gastrocnemius muscle and some anatomists consider them to be a single muscle, the triceps
            surae. Its name is derived from the Latin word "solea", meaning "sandal".</p>

        <h3>Structure</h3>
        <p>The soleus is located in the superficial posterior compartment of the leg.</p>
        <p>The soleus exhibits significant morphological differences across species. It is unipennate in many species.
            In some animals, such as the rabbit, it is fused for much of its length with the gastrocnemius muscle.</p>
        <p>In the human, soleus is a complex multi-pennate muscle, usually having a separate (posterior) aponeurosis
            from the gastrocnemius muscle. A majority of soleus muscle fibers originate from each side of the anterior
            aponeurosis, attached to the tibia and fibula. Other fibers originate from the posterior (back) surfaces of
            the head of the fibula and its upper quarter, as well as the middle third of the medial border of the tibia.
        </p>
        <p>The fibers originating from the anterior surface of the anterior aponeurosis insert onto the median septum
            and the fibers originating from the posterior surface of the anterior aponeurosis insert onto the posterior
            aponeurosis. The posterior aponeurosis and median septum join in the lower quarter of the muscle and then
            join with the anterior aponeuroses of the gastrocnemius muscles to form the calcaneal tendon or Achilles
            tendon and inserts onto the posterior surface of the calcaneus, or heel bone.</p>
        <p>In contrast to some animals, the human soleus and gastrocnemius muscles are relatively separate, such that
            shear can be detected between the soleus and gastrocnemius aponeuroses.</p>
        <p>The Soleus is vestigial in the horse.</p>

        <h3>Nerve Supply</h3>
        <p>Tibial nerve, L4, L5, S1 , S2</p>

        <h3>Function</h3>
        <p>The action of the calf muscles, including the soleus, is plantarflexion of the foot (that is, they increase
            the angle between the foot and the leg). They are powerful muscles and are vital in walking, running, and
            keeping balance. The soleus specifically plays an important role in maintaining standing posture; if not for
            its constant pull, the body would fall forward.</p>
        <p>Also, in upright posture, the soleus is responsible for pumping venous blood back into the heart from the
            periphery, and is often called the skeletal-muscle pump, peripheral heart or the sural (tricipital) pump.
        </p>
        <p>Soleus muscles have a higher proportion of slow muscle fibers than many other muscles. In some animals, such
            as the guinea pig and cat, soleus consists of 100% slow muscle fibers. Human soleus fiber composition is
            quite variable, containing between 60 and 100% slow fibers.</p>
        <p>The soleus is the most effective muscle for plantarflexion in a bent knee position (Hence called the first
            gear muscle). This is because the gastrocnemius originates on the femur, so bending the leg limits its
            effective tension. During regular movement (i.e., walking) the soleus is the primary muscle utilized for
            plantarflexion due to the slowtwitch fibers resisting fatigue.</p>

