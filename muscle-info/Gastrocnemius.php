        <h2>Gastrocnemius</h2>
        <p>The gastrocnemius muscle is a superficial two-headed muscle that is in the back part of
            the lower leg of humans. It runs from its two heads just above the knee to the heel, a three joint muscle
            (knee, ankle and subtalar joints). The muscle is named via Latin, from Greek γαστήρ (gaster) 'belly' or
            'stomach' and κνήμη (knḗmē) 'leg', meaning 'stomach of leg' (referring to the bulging shape of the calf).
        </p>

        <h3>Structure</h3>
        <p>The gastrocnemius is located with the soleus in the posterior (back) compartment of the leg. The lateral head
            originates from the lateral condyle of the femur, while the medial head originates from the medial condyle
            of the femur. Its other end forms a common tendon with the soleus muscle; this tendon is known as the
            calcaneal tendon or Achilles tendon and inserts onto the posterior surface of the calcaneus, or heel bone.
            It is considered a superficial muscle as it is located directly under skin, and its shape may often be
            visualized through the skin.</p>
        <p>Deep to the gastrocnemius (farther from the skin) is the soleus muscle. Some anatomists consider both to be a
            single muscle—the triceps surae or "three-headed [muscle] of the calf"—since they share a common insertion
            via the Achilles tendon. The plantaris muscle and a portion of its tendon run between the two muscles, which
            is involved in "locking" the knee from the standing position. Since the anterior compartment of the leg is
            lateral to the tibia, the bulge of muscle medial to the tibia on the anterior side is actually the posterior
            compartment. The soleus is superficial to the mid-shaft of the tibia.</p>

        <h3>Nerve Supply</h3>
        <p>Tibial nerve from the sciatic, specifically, nerve roots S1–S2</p>

        <h3>Function</h3>
        <p>Along with the soleus muscle, the gastrocnemius forms half of the calf muscle. Its function is plantar
            flexing the foot at the ankle joint and flexing the leg at the knee joint.</p>
        <p>The gastrocnemius is primarily involved in running, jumping and other "fast" movements of leg, and to a
            lesser degree in walking and standing. This specialization is connected to the predominance of white muscle
            fibers (type II fast twitch) present in the gastrocnemius, as opposed to the soleus, which has more red
            muscle fibers (type I slow twitch) and is the primary active muscle when standing still, as determined by
            EMG studies.</p>
        <p><span>Motor pathway</span></p>
        <p>The plan to use the gastrocnemius in running, jumping, knee and plantar flexing is created in the precentral
            gyrus in the cerebrum of the brain.[3] Once a plan is produced, the signal is sent to and down an upper
            motor neuron. The signal is passed through the internal capsule and decussates, or crosses, in the medulla
            oblongata, specifically in the lateral corticospinal tract.[4] The signal continues down through the
            anterior horn of the spinal cord where the upper motor neuron synapses with the lower motor neuron. Signal
            propagation continues down the anterior rami (Lumbar 4-5 and Sacral 1-5) of the sacral plexus. The sciatic
            nerve branches off of the sacral plexus in which the tibial and common fibular nerves are wrapped in one
            sheath. The tibial nerve eventually separates from the sciatic nerve and innervates the gastrocnemius
            muscle. Thus, completing the plan the brain had originally started with, so that the actions of running,
            standing, and jumping could be executed.</p>

