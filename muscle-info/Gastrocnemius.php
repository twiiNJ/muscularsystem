            <h3>Seated Alternating Hammer Curl</h3>
            <p>While not necessarily the most well-known bicep exercise, the Hammer Curl is a favourite among
                bodybuilders and strongmen alike. This Seated Alternating Hammer Curl variation allows you to focus
                on each arm individually, concentrating on singular growth and isolation. Put simply, the Hammer
                Curl allows you to switch muscle groups, strengthening your biceps brachialis, a deeper muscle that
                will aid in overall strength and shape. To perform a Seated Alternating Hammer Curl;</p>
            <ol>
                <li>Sit on a 90-degree bench with a dumbbell in each hand, palms facing in toward your body and arms
                    extended straight toward the floor.</li>
                <li>Keeping your elbow and shoulder stationary, slowly lift one weight to the front of your
                    shoulder.</li>
                <li>Pause, squeezing your bicep at the top.</li>
                <li>Slowly reverse the movement to return to start.</li>
                <li>Repeat on the opposite side.</li>
            </ol>
